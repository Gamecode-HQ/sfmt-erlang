-module(random_wh06_tests).

-export([dump/0, dump_next_sequence/0]).

%% @doc dumping reference output for the regression test
%% of uniform/0

dump_rec1(0, Acc) ->
     lists:reverse(Acc);
dump_rec1(I, Acc) ->
     V = random_wh06:uniform(),
     dump_rec1(I - 1, [{V, get(random_wh06_seed)}| Acc]).

dump() ->
    put(random_wh06_seed, {123456789, 345678901, 567890123, 789012345}),
    io:format("~p~n", [dump_rec1(1000, [])]),
    ok.

%% @doc dumping reference output for the regression test
%% of next_sequence/1

dump_next_sequence_rec1(0, Acc, _) ->
    lists:reverse(Acc);
dump_next_sequence_rec1(I, Acc, S) ->
    {V, S0} = random_wh06:uniform_s(S),
    S1 = random_wh06:next_sequence(S0), 
    dump_next_sequence_rec1(I - 1, [{V, S0}| Acc], S1).

dump_next_sequence() ->
    io:format("~p~n", 
	      [dump_next_sequence_rec1(
		 100, [], 
		 random_wh06:next_sequence(
		   {123456789, 345678901, 567890123, 789012345}
		   ))]),
    ok.

%% EUnit test functions

-ifdef(TEST).

-include_lib("eunit/include/eunit.hrl").

%% @doc seeding tests

seed_tests() ->
    random_wh06:seed(100,1000,10000,100000),
    S1 = get(random_wh06_seed),
    ?assertEqual({101,1001,10001,100001}, S1),
    random_wh06:seed(10000000000,100000000000,10000000000000,100000000000000),
    S2 = get(random_wh06_seed),
    ?assertEqual({1410065689,1215757069,1317187169,300940949}, S2),
    random_wh06:seed(0,2,3,4),
    S3 = get(random_wh06_seed),
    ?assertEqual({1,3,4,5}, S3), 
    random_wh06:seed(1,0,3,4),
    S4 = get(random_wh06_seed),
    ?assertEqual({2,1,4,5}, S4),
    random_wh06:seed(1,2,0,4),
    S5 = get(random_wh06_seed),
    ?assertEqual({2,3,1,5}, S5),
    random_wh06:seed(1,2,3,0),
    S6 = get(random_wh06_seed),
    ?assertEqual({2,3,4,1}, S6),
    random_wh06:seed(),
    S7 = get(random_wh06_seed),
    ?assertEqual({123456790,345678902,567890124,789012346}, S7).

%% @doc random_wh06:uniform/0 value loop (without output)

uniform_test1_rec1(0, Acc) ->
    lists:reverse(Acc);
uniform_test1_rec1(I, Acc) ->
    random_wh06:uniform(),
    uniform_test1_rec1(I - 1, [get(random_wh06_seed)| Acc]).

%% @doc random_wh06:uniform/0 value loop (with output)

uniform_test2_rec1(0, Acc) ->
    lists:reverse(Acc);
uniform_test2_rec1(I, Acc) ->
    V = random_wh06:uniform(),
    uniform_test2_rec1(I - 1, [{V, get(random_wh06_seed)}| Acc]).

%% @doc random_wh06:uniform_s/1 value loop (with output and state)

uniform_s_test3_rec1(0, Acc, _) ->
    lists:reverse(Acc);
uniform_s_test3_rec1(I, Acc, RS) ->
    {V, RS2} = random_wh06:uniform_s(RS),
    uniform_s_test3_rec1(I - 1, [{V, RS2} | Acc], RS2).

%% @doc random_wh06:next_sequence/1 test loop

next_sequence_rec1(0, Acc, _) ->
    lists:reverse(Acc);
next_sequence_rec1(I, Acc, S) ->
    {V, S0} = random_wh06:uniform_s(S),
    S1 = random_wh06:next_sequence(S0), 
    next_sequence_rec1(I - 1, [{V, S0}| Acc], S1).

%% @doc Value tests of the first 1000 random numbers 
%% Note: internal state only

value_tests_1() ->
    Reffloat = test_refval(),
    Reflist = [S || {_, S} <- Reffloat],
    put(random_wh06_seed, {123456789, 345678901, 567890123, 789012345}),
    Out1 = uniform_test1_rec1(1000, []),
    ?assertEqual(Reflist, Out1).

%% @doc Value tests of the first 1000 random numbers 
%% including float output

value_tests_2() ->
    Reffloat = test_refval(),
    put(random_wh06_seed, {123456789, 345678901, 567890123, 789012345}),
    Out2 = uniform_test2_rec1(1000, []),
    ?assertEqual(Reffloat, Out2).

%% @doc Value tests of the first 1000 random numbers 
%% with explicit state

value_tests_3() ->
    Reffloat = test_refval(),
    Out3 = uniform_s_test3_rec1(1000, [], {123456789, 345678901, 567890123, 789012345}),
    ?assertEqual(Reffloat, Out3).

%% @doc Value tests of first 100 calls of next_sequence/1

value_tests_4() ->
    Refnext = test_refval_next_sequence(),
    Out4 = next_sequence_rec1(
		 100, [], 
		 random_wh06:next_sequence(
		   {123456789, 345678901, 567890123, 789012345}
		   )),
    ?assertEqual(Refnext, Out4).

%% @doc simple testing function as used in EUnit

simple_test_() -> 
    [
     ?_assertMatch(ok, seed_tests()),
     ?_assertMatch(ok, value_tests_1()),
     ?_assertMatch(ok, value_tests_2()),
     ?_assertMatch(ok, value_tests_3()),
     ?_assertMatch(ok, value_tests_4())
    ].

